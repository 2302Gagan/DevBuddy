/** Translate a UI key into the active locale, falling back to English. */
export function translate(lang: UiLanguage, key: keyof UiCopyShape): string {
  return UI_COPY[lang]?.[key] ?? UI_COPY.en[key]
}

/** Export key type so callers can use it for strict key checking. */
